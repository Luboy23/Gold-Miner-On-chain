const LEGACY_LEVEL_DEFINITIONS: Record<string, LegacyLevelDefinition> = {
  ...EARLY_LEVEL_DEFINITIONS,
  ...SPRINT4_LEVEL_DEFINITIONS,
};

function renameLegacyLevel(
  sourceId: string,
  targetId: string,
  group: LevelGroup,
): LevelDefinition {
  const source = LEGACY_LEVEL_DEFINITIONS[sourceId];

  if (!source) {
    throw new Error(`Missing legacy level definition: ${sourceId}`);
  }

  return {
    id: targetId,
    group,
    theme: source.theme,
    timeLimitSec: source.timeLimitSec,
    entities: source.entities.map((entity) => ({ ...entity })),
  };
}

